from __future__ import annotations

from datetime import datetime, timedelta
from typing import Any


def _schedules(
    now: datetime,
    *,
    appraisal: int,
    fail_count: int,
    days_to_sale: int,
    current_min: int,
) -> list[dict[str, Any]]:
    """Generate realistic round history with ~20% step-downs after fail."""
    rows: list[dict[str, Any]] = []
    price = int(appraisal * 0.8)
    for i in range(1, fail_count + 2):
        if i == fail_count + 1:
            price = current_min
            sale = (now + timedelta(days=days_to_sale)).date()
            result = "진행"
        else:
            if i > 1:
                price = int(price * 0.8)
            sale = (now - timedelta(days=(fail_count + 1 - i) * 14)).date()
            result = "유찰"
        rows.append(
            {
                "round_no": i,
                "sale_date": sale.isoformat(),
                "min_bid_manwon": price,
                "result": result,
                "note": f"{i}회차 {'매각기일' if result == '진행' else '유찰'}",
            }
        )
    return rows


